package dict

import (
	"math"
	"strings"
)

func Exists(s string) bool {
	return words[s]
}

func AllWords() []string {
	var ret []string
	for k := range words {
		ret = append(ret, k)
	}
	return ret
}

func Score(msg string) float64 {
	ws := strings.Fields(strings.Map(func(r rune) rune {
		switch {
		case 'A' <= r && r <= 'Z':
			return r
		case r == ' ', r == ',', r == '<', r == '>', r == '.', r == '~', r == '*', r == '!', r == '+', r == '#', r == '"', r == '\'':
			return ' '
		default:
			return -1
		}
	}, msg))
	if len(ws) == 0 {
		return 0.0
	}
	total := 0
	score := 0.0
	for _, word := range ws {
		total += len(word)
		if Exists(word) {
			score += math.Pow(float64(len(word)), 1.0)
		}
	}
	return score / float64(total)
}

func buildVariants(s string) []string {
	substitutions := map[string]string{
		"QU": "CW",
		"K":  "C",
		"V":  "U",
	}
	s = strings.ToUpper(s)
	for k, v := range substitutions {
		s = strings.ReplaceAll(s, k, v)
	}
	var ret []string
	ret = append(ret, s)
	ret = append(ret, strings.ReplaceAll(s, "NG", "ING"))
	return ret
}

func init() {
	for word := range smallWords {
		variants := buildVariants(word)
		for _, variant := range variants {
			words[variant] = true
		}
	}
	for word := range baseWords {
		if len(word) < 4 {
			continue
		}
		variants := buildVariants(word)
		for _, variant := range variants {
			words[variant] = true
		}
	}
}

